export const buildPublicBreakdown = (umkm) =>
  evidenceIndicatorDetails.map((indicator) => {
    const matched = umkm.breakdown.find((item) => item.slug === indicator.slug)
    const score = matched?.score ?? indicator.score

    return {
      ...indicator,
      score,
    }
  })

export const findPublicUmkmBySlug = (slug) => publicUmkmList.find((item) => item.slug === slug)
